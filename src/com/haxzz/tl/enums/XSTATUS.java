package com.haxzz.tl.enums;

public enum XSTATUS {
	
	UNBANNED(0),
	NO_IP_ADDRESS(0x80151901),
	NO_DNS_SERVICE(0x80151902),
	DNS_LOOKUP_FAILED(0x80151903),
	DNS_LOOKUP_TIMEDOUT(0x80151904),
	INVALID_XBOX_ONLINE_INFO(0x80151905),
	MACS_FAILED(0x80151906),
	MACS_TIMEDOUT(0x80151907),
	AUTHENTICATION_FAILED(0x80151908),
	AUTHENTICATION_TIMEDOUT(0x80151909),
	AUTHORIZATION_FAILED(0x8015190A),
	AUTHORIZATION_TIMEDOUT(0x8015190B),
	XBOX_ACCOUNT_INVALID(0x8015190C),
	XBOX_ACCOUNT_BANNED(0x8015190D),
	SG_SERVICE_FAILED(0x8015190E),
	PRESENCE_SERVICE_FAILED(0x8015190F),
	PRESENCE_SERVICE_TIMEDOUT(0x80151910),
	TIMEDOUT(0x80151911),
	UNKNOWN_TITLE(0x80151912),
	INTERNAL_ERROR(0x80151913),
	MACHINE_AUTHENTICATION_FAILED(0x80151914),
	TGT_REVOKED(0x80151915),
	CACHE_MISS(0x80151916),
	NOT_UPNP_NAT(0x80151917),
	INCONCLUSIVE_UPNP_NAT(0x80151918),
	UPNP_NAT_HARD_FAILURE(0x80151919),
	UPNP_PORT_UNAVAILABLE(0x8015191A);
	
	private int value;
	
	private XSTATUS(int value) {
		this.value = value;
	}
	
	public short getValue() {
        return (short)this.value;
    }
	
	public static XSTATUS parseValue(int value){
		for (XSTATUS i : XSTATUS.values()){
			if(i.getValue() == value){
				return i;
			}
		}
		return UNBANNED;
	}

}
